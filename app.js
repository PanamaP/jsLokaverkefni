// Template

var div = document.createElement("div");
div.id = "container";
document.body.appendChild(div);
divID = document.getElementById("container");


for(i in uppskriftir){

    // create div for each recipe with class & id
    divItem = document.createElement("div");
    divItem.classList.add("recipe");
    divItem.id = "recipe_"+i;

    // show recipe name
    p = document.createElement("p");
    recipe = document.createTextNode(`Recipe: ${uppskriftir[i].recipe}`)
    divItem.appendChild(p).appendChild(recipe);

    // show author name
    p = document.createElement("p");
    author = document.createTextNode(`Author: ${uppskriftir[i].author}`);
    divItem.appendChild(p).appendChild(author);

    // show date
    p = document.createElement("p");
    date = document.createTextNode(uppskriftir[i].date);
    divItem.appendChild(p).appendChild(date);


    var picture = document.createElement("IMG");
    picture.setAttribute("src", uppskriftir[i].picture);
    picture.setAttribute("width", "200");
    picture.setAttribute("height", "200");
    divItem.appendChild(picture);


    // show ingredients
    ul = document.createElement("ul");
    ingredientsHead = document.createTextNode(`Ingredients:`)
    divItem.appendChild(ul).appendChild(ingredientsHead);

    // get every ingredient in recipe and show in list
    for(x in uppskriftir[i].ingredients){
        li = document.createElement("li");
        ingredients = document.createTextNode(uppskriftir[i].ingredients[x]);
        ul.appendChild(li).appendChild(ingredients);
    }

    // show instructions
    p = document.createElement("p");
    instructionsHead = document.createTextNode(`Instructions: `);
    divItem.appendChild(p).appendChild(instructionsHead);

    // get every step in instructions and show
    for(j in uppskriftir[i].instructions){
        test = document.createElement('p');
        instructions = document.createTextNode(uppskriftir[i].instructions[j]);
        divItem.appendChild(test).appendChild(instructions);
    }    

    
    div.appendChild(divItem);
};



